class IfElse {
   public static void main(String[] args) {    	
      int number = 10;
	 
      if (number < 10) {
	System.out.println("Number is Positive.");
      	}

      else {
         	System.out.println("Number is Negative.");
     	 }
 
      	System.out.println("This statement is always executed.");
   }	//main method
}	// class